import java.util.*;
public class sumofseries {
    public static void main(String[] args) {
        Scanner in=new Scanner(System.in);
        int x=in.nextInt();
        int sum=0;
        for(int i=0;i<0/1;i++)
    
        {
           sum+=x/i;
        }   
        System.out.println(sum);
    }
    
}
