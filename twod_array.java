
public class twod_array {
    public static void main(String args[])
    {
        int marks[][]={
            {10,20},
            {30,40},
            {40,60}
          };
          for(int i=0;i<3;i++)
          {
              for(int j=0;j<2;j++)
              {
                System.out.println(marks[i][j]);
              }
          }
    }
    
}
